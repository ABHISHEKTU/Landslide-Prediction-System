from google.colab import drive
drive.mount('/content/drive')

import os
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

from tensorflow.keras.preprocessing.image import load_img, img_to_array
from sklearn.model_selection import train_test_split
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score

from tensorflow.keras.layers import Input, Conv2D, MaxPooling2D, Flatten, Dense, Concatenate
from tensorflow.keras.models import Model
from tensorflow.keras.optimizers import Adam

print("✅ Libraries loaded successfully")

# =========================
# PATHS
# =========================

BASE_PATH = "/content/drive/MyDrive/landslide_new/Bijie-landslide-dataset"
CSV_PATH = os.path.join(BASE_PATH, "bijie_binary_labels.csv")

SAVE_DIR = "/content/drive/MyDrive/landslide_new"
MODEL_PATH = os.path.join(SAVE_DIR, "landslide_model_new.h5")

print("📁 Dataset Path:", BASE_PATH)
print("📁 Save Path   :", SAVE_DIR)

# =========================
# CELL 2: LOAD DATA (FINAL)
# =========================

IMG_SIZE = (224, 224)

df = pd.read_csv(CSV_PATH)

print("\n📄 First 5 rows:")
print(df.head())

# =========================
# CHECK DATASET STRUCTURE
# =========================

print("\n📂 Checking dataset folders...")

print("BASE:", os.listdir(BASE_PATH))
print("LANDSLIDE:", os.listdir(os.path.join(BASE_PATH, "landslide")))
print("NON-LANDSLIDE:", os.listdir(os.path.join(BASE_PATH, "non-landslide")))

print("\n🔍 Sample image files:")
print(os.listdir(os.path.join(BASE_PATH, "non-landslide", "image"))[:5])

print("\n🔍 Sample DEM files:")
print(os.listdir(os.path.join(BASE_PATH, "non-landslide", "dem"))[:5])

# =========================
# IMAGE LOADER
# =========================

def load_image(path, grayscale=False):
    mode = "grayscale" if grayscale else "rgb"
    img = load_img(path, target_size=IMG_SIZE, color_mode=mode)
    img = img_to_array(img) / 255.0
    return img

sat_images = []
dem_images = []
labels = []

print("\n📥 Loading dataset...")

sample_print_count = 0
missing_count = 0

import matplotlib.pyplot as plt

for i, row in df.iterrows():

    # =========================
    # FIX WINDOWS PATH ISSUE
    # =========================
    raw_path = row['image_path']
    img_name = os.path.basename(raw_path.replace("\\", "/"))

    label = row['label']
    folder = "landslide" if label == 1 else "non-landslide"

    img_path = os.path.join(BASE_PATH, folder, "image", img_name)
    dem_path = os.path.join(BASE_PATH, folder, "dem", img_name)

    # =========================
    # DEBUG PRINT (FIRST 5)
    # =========================
    if sample_print_count < 5:
        print(f"\n🔍 Sample {sample_print_count+1}")
        print("Raw Path :", raw_path)
        print("IMG Path :", img_path)
        print("DEM Path :", dem_path)
        print("Label    :", label)

    # =========================
    # CHECK FILE EXISTENCE
    # =========================
    if not os.path.exists(img_path):
        missing_count += 1
        if sample_print_count < 5:
            print("❌ Missing IMG")
        continue

    if not os.path.exists(dem_path):
        missing_count += 1
        if sample_print_count < 5:
            print("❌ Missing DEM")
        continue

    # =========================
    # LOAD IMAGES
    # =========================
    try:
        sat = load_image(img_path)
        dem = load_image(dem_path, grayscale=True)

        sat_images.append(sat)
        dem_images.append(dem)
        labels.append(label)

        if sample_print_count < 5:
            print("✅ Loaded successfully")

            # =========================
            # SHOW IMAGES
            # =========================
            plt.figure(figsize=(6,3))

            # Satellite
            plt.subplot(1,2,1)
            plt.imshow(sat)
            plt.title("Satellite")
            plt.axis('off')

            # DEM
            plt.subplot(1,2,2)
            plt.imshow(dem.squeeze(), cmap='gray')
            plt.title("DEM")
            plt.axis('off')

            plt.show()

        sample_print_count += 1

    except Exception as e:
        print("⚠️ Error loading:", img_name, "|", e)

# =========================
# CONVERT TO NUMPY
# =========================

X_sat = np.array(sat_images)
X_dem = np.array(dem_images)
y = np.array(labels)

print("\n✅ Dataset Summary:")
print("Satellite shape:", X_sat.shape)
print("DEM shape      :", X_dem.shape)
print("Labels shape   :", y.shape)
print("❌ Missing files:", missing_count)

# =========================
# SAFETY CHECK
# =========================

if len(X_sat) == 0:
    raise ValueError("🚨 No data loaded! Check dataset path or CSV.")

# =========================
# CELL 3: MODEL + TRAIN
# =========================

X_sat_train, X_sat_test, X_dem_train, X_dem_test, y_train, y_test = train_test_split(
    X_sat, X_dem, y, test_size=0.2, random_state=42
)

print("✅ Data split done")

# MODEL

sat_input = Input(shape=(224,224,3))
x1 = Conv2D(32, (3,3), activation='relu')(sat_input)
x1 = MaxPooling2D()(x1)
x1 = Conv2D(64, (3,3), activation='relu')(x1)
x1 = MaxPooling2D()(x1)
x1 = Flatten()(x1)

dem_input = Input(shape=(224,224,1))
x2 = Conv2D(16, (3,3), activation='relu')(dem_input)
x2 = MaxPooling2D()(x2)
x2 = Conv2D(32, (3,3), activation='relu')(x2)
x2 = MaxPooling2D()(x2)
x2 = Flatten()(x2)

merged = Concatenate()([x1, x2])

x = Dense(128, activation='relu')(merged)
x = Dense(64, activation='relu')(x)
output = Dense(1, activation='sigmoid')(x)

model = Model(inputs=[sat_input, dem_input], outputs=output)

model.compile(
    optimizer=Adam(learning_rate=0.0001),
    loss='binary_crossentropy',
    metrics=['accuracy']
)

model.summary()

print("\n🚀 Training started...")

history = model.fit(
    [X_sat_train, X_dem_train],
    y_train,
    validation_data=([X_sat_test, X_dem_test], y_test),
    epochs=30,
    batch_size=8
)

print("✅ Training completed")

# =========================
# CELL 4: EVALUATION + SAVE
# =========================

# Predictions
y_pred = model.predict([X_sat_test, X_dem_test])
y_pred = (y_pred > 0.5).astype(int)

# Metrics
accuracy = accuracy_score(y_test, y_pred)
precision = precision_score(y_test, y_pred)
recall = recall_score(y_test, y_pred)
f1 = f1_score(y_test, y_pred)

print("\n📊 RESULTS:")
print("Accuracy :", accuracy)
print("Precision:", precision)
print("Recall   :", recall)
print("F1 Score :", f1)

# Save results
results_path = os.path.join(SAVE_DIR, "results.txt")

with open(results_path, "w") as f:
    f.write(f"Accuracy: {accuracy}\n")
    f.write(f"Precision: {precision}\n")
    f.write(f"Recall: {recall}\n")
    f.write(f"F1 Score: {f1}\n")

print("📄 Results saved:", results_path)

# =========================
# GRAPH
# =========================

# Accuracy Graph
plt.figure()
plt.plot(history.history['accuracy'])
plt.plot(history.history['val_accuracy'])
plt.title('Model Accuracy')
plt.xlabel('Epoch')
plt.ylabel('Accuracy')
plt.legend(['Train', 'Val'])

acc_path = os.path.join(SAVE_DIR, "accuracy.png")
plt.savefig(acc_path)
plt.show()

# Loss Graph
plt.figure()
plt.plot(history.history['loss'])
plt.plot(history.history['val_loss'])
plt.title('Model Loss')
plt.xlabel('Epoch')
plt.ylabel('Loss')
plt.legend(['Train', 'Val'])

loss_path = os.path.join(SAVE_DIR, "loss.png")
plt.savefig(loss_path)
plt.show()

print("📊 Graphs saved")

# =========================
# SAVE MODEL
# =========================

model.save(MODEL_PATH)

print("\n💾 Model saved at:", MODEL_PATH)